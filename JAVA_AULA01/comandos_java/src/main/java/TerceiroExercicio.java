public class TerceiroExercicio {
    public static void main(String [] args){
        int num = 8;
        int ndois = 5;
        int soma = num + ndois;
        int divsao = num / ndois;
        int sub = num - ndois;
        int mult = num * ndois;

        System.out.println("A Soma de "+num+" + "+ndois+" e = "+soma);
        System.out.println("A Divisao de "+num+" / "+ndois+" e = "+divsao);
        System.out.println("A subtracao de "+num+" - "+ndois+" e = "+sub);
        System.out.println("A Multiplicacao de "+num+" X "+ndois+" e = "+mult);
    }
}
